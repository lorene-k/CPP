#include "libft.h"

int     ft_isoperator(char c)
{
    if (c == '+' || c == '-' || c == '*' || c == '/' || c == '%'
        || c == '=' || c == '!' || c == '&' || c == '|' || c == '^'
        || c == '<' || c == '>')
        return (1);
    else
        return (0);
}
